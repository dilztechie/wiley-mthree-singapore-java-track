public class DataTypes {
	public static void main(String[] args) {
		/*
		 * // Declaration of Variable/Member byte byteValue; char charValue; short
		 * shortValue; int intValue; long longValue; float floatValue; double
		 * doubleValue; boolean booleanValue;
		 * 
		 * // Assignment byteValue = 127; charValue = 'D'; shortValue = 32767; intValue
		 * = 2147483647; longValue = 9223372036854775807L; // prefix with l or L
		 * floatValue = 3.40282347e38f; // prefix with f or F doubleValue =
		 * 4.9406564584124654; // x 10^308 booleanValue = false;
		 */

		// Combine Declaration with Assignment
		byte byteValue = 127;
		char charValue = 'D';
		short shortValue = 32767;
		int intValue = 2147483647;
		long longValue = 9223372036854775807L;
		float floatValue = 3.40282347e38f;
		double doubleValue = 4.9406564584124654;
		boolean booleanValue = false;

		
		System.out.println("Byte Value = " + byteValue);
		System.out.println("Character Value = " + charValue);
		System.out.println("Short Value = " + shortValue);
		System.out.println("Integer Value = " + intValue);
		System.out.println("Long Value = " + longValue);
		System.out.println("Float Value = " + floatValue);
		System.out.println("Double Value = " + doubleValue);
		System.out.println("Boolean Value = " + booleanValue);
		 
	}
}